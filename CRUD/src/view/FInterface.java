    private void botaoCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastroActionPerformed
        // TODO add your handling code here:
        Produto produto = new Produto();
        produto.setNome(fNome.getText());
        produto.setPreco(Float.parseFloat(fPreco.getText()));
        produto.setQuantidade(Integer.parseInt(fQuantidade.getText()));
        
        ProdutoDAO produtoDAO = new ProdutoDAO();
        produtoDAO.inserir(produto);
        
        
    }//GEN-LAST:event_botaoCadastroActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        produtos = null;
        
        DefaultTableModel tabelaPadrao = (DefaultTableModel) tabelaProdutos.getModel();
        tabelaPadrao.setNumRows(0);
        
        ProdutoDAO produtoDAO = new ProdutoDAO();
        produtos = produtoDAO.buscar(fBusca.getText().trim());
        
        for(int i = 0; i < produtos.size(); i++){
            Produto p = new Produto();
            p = produtos.get(i);
            
            tabelaPadrao.addRow(new Object[] {
                p.getId(), p.getNome(), p.getPreco(), p.getQuantidade()
        });
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

