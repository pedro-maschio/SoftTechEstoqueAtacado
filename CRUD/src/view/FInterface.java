    private void botaoVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoVenderActionPerformed
        // TODO add your handling code here:
        int linha_selecionada = tabelaProdutos.getSelectedRow();
        if(linha_selecionada == -1){
            JOptionPane.showMessageDialog(null, "Selecione um produto!");
        } else {
        
        setVisible(false);
        VendaProdutos venda = new VendaProdutos();
        venda.setVisible(true);
        
        // getValueAt retona um Object, por isso o toString(); 
        TableModel modelo = (TableModel) tabelaProdutos.getModel();
        int codigo = Integer.parseInt(modelo.getValueAt(linha_selecionada, 0).toString());
        String nome = modelo.getValueAt(linha_selecionada, 1).toString();
        float preco =  Float.parseFloat(modelo.getValueAt(linha_selecionada, 2).toString());
        int quantidade = Integer.parseInt(modelo.getValueAt(linha_selecionada, 3).toString());
        
        venda.preencheCampos(codigo, nome, preco, quantidade);
        }
    }//GEN-LAST:event_botaoVenderActionPerformed

